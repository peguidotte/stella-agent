# Armazenamento simples em mem√≥ria para sess√µes Gemini
_SESSIONS: Dict[str, Any] = {}  # Any porque ChatSession n√£o est√° em genai.types
_LAST_SEEN: Dict[str, float] = {}
_SESSION_TTL_SECONDS = 3 * 60  # 3 minutos
ACTIVE_SESSION_ID: Optional[str] = None  # sess√£o ativa exclusiva

def _gc_sessions():
    """Coleta sess√µes inativas (TTL)."""
    now = time.time()
    expired = [sid for sid, ts in _LAST_SEEN.items() if now - ts > _SESSION_TTL_SECONDS]
    for sid in expired:
        _SESSIONS.pop(sid, None)
        _LAST_SEEN.pop(sid, None)
        logger.info(f"Sess√£o expirada e removida: {sid}")

def get_or_create_session(session_id: str) -> Any:
    """Retorna ou cria uma ChatSession por session_id."""
    if not session_id:
        raise ValueError("session_id √© obrigat√≥rio para manter contexto.")
    sess = _SESSIONS.get(session_id)
    if sess is None:
        sess = model.start_chat(history=[])
        _SESSIONS[session_id] = sess
        logger.info(f"Sess√£o criada: {session_id}")
    _LAST_SEEN[session_id] = time.time()
    _gc_sessions()
    return sess

def end_session(session_id: str) -> bool:
    """
    Encerra uma sess√£o espec√≠fica do Gemini
    
    Args:
        session_id: ID da sess√£o para encerrar
        
    Returns:
        True se sess√£o foi encontrada e removida
    """
    global _SESSIONS, _LAST_SEEN
    
    if session_id in _SESSIONS:
        del _SESSIONS[session_id]
        if session_id in _LAST_SEEN:
            del _LAST_SEEN[session_id]
        logger.info(f"üóëÔ∏è Sess√£o Gemini encerrada: {session_id}")
        return True
    
    logger.warning(f"‚ö†Ô∏è Sess√£o n√£o encontrada para encerrar: {session_id}")
    return False

def switch_active_session(session_id: str):
    """Garante sess√£o exclusiva: ao trocar de session_id, apaga hist√≥rico anterior."""
    global ACTIVE_SESSION_ID
    if ACTIVE_SESSION_ID and ACTIVE_SESSION_ID != session_id:
        for sid in list(_SESSIONS.keys()):
            if sid != session_id:
                end_session(sid)
        logger.info(f"Alternando sess√£o ativa de {ACTIVE_SESSION_ID} para {session_id}")
    ACTIVE_SESSION_ID = session_id
async def load_external_stock(base_url: Optional[str] = None) -> Dict[str, Any]:
    """
    Busca estoque da API externa e retorna um dicion√°rio indexado por nome normalizado.
    Estrutura da API externa (GET /products):
    [
      {
        "id": "string",
        "name": "string",
        "description": "string",
        "quantity": 0,
        "createdAt": "2025-09-24T04:12:55.092Z",
        "updatedAt": "2025-09-24T04:12:55.093Z",
        "categoryName": "string"
      }
    ]
    """
    # Permite configurar via vari√°vel de ambiente INVENTORY_API_URL
    base = (base_url or os.environ.get("INVENTORY_API_URL") or "http://localhost:8080").rstrip('/')
    url = base + "/products"
    try:
        async with httpx.AsyncClient(timeout=10.0) as client:
            resp = await client.get(url)
            resp.raise_for_status()
            items = resp.json() or []
            def _norm(s: str) -> str:
                s = (s or "").strip().lower()
                # normaliza espa√ßos e separadores comuns
                for ch in [" ", ",", ".", ";", ":", "/", "\\"]:
                    s = s.replace(ch, "_")
                while "__" in s:
                    s = s.replace("__", "_")
                return s.strip("_")
            stock_map: Dict[str, Any] = {}
            for it in items:
                name = it.get("name") or ""
                norm = _norm(name)
                stock_map[norm] = {
                    "id": it.get("id"),
                    "name": name,
                    "description": it.get("description"),
                    "quantity": int(it.get("quantity", 0)),
                    "createdAt": it.get("createdAt"),
                    "updatedAt": it.get("updatedAt"),
                    "categoryName": it.get("categoryName"),
                }
            return stock_map
    except httpx.HTTPError as e:
        logger.error(f"Erro ao consultar estoque externo: {e}")
        return {}
    except Exception as e:
        logger.error(f"Erro inesperado ao consultar estoque externo: {e}")
        return {}
    
    
async def publish_withdraw_confirm(session_id: str, items: list):
    """
    Publica confirma√ß√£o de retirada no RabbitMQ de forma ass√≠ncrona
    """
    try:
        
        
        payload = {
            "itens": items,
            "withdrawBy": session_id 
        }
        
        logger.debug(f"Preparando para publicar confirma√ß√£o de retirada | `{payload}`")
        
        await asyncio.to_thread(publish, 'stock', 'remove', payload)
        logger.success(f"üì§ Confirma√ß√£o de retirada publicada | Sess√£o: {session_id}")
        
    except Exception as e:
        logger.error(f"‚ùå Erro ao publicar confirma√ß√£o: {e}")

async def command_interpreter(comando: str, session_id: str):
    switch_active_session(session_id)
    sess = get_or_create_session(session_id)
    # Carrega estoque da API externa
    external_stock = await load_external_stock()
    
    # Formatar estoque de forma leg√≠vel para a IA
    estoque_formatado = ""
    for norm_name, item in external_stock.items():
        quantidade_atual = item.get('quantity', 0)
        categoria = item.get('categoryName') or 'N/A'
        estoque_formatado += (
            f"\n‚Ä¢ {norm_name}: {item.get('name')}\n"
            f"- Quantidade atual: {quantidade_atual} unidade(s)\n"
            f"- Categoria: {categoria}\n"
        )
    
    prompt = f"""
        Analise este comando: "{comando}"

        ESTOQUE ATUAL:
        {estoque_formatado}
        """
    
    try:
        # ‚úÖ Usar asyncio.to_thread para opera√ß√£o s√≠ncrona do Gemini
        response = await asyncio.to_thread(sess.send_message, prompt)
        
        # Limpa a resposta
        clean_text = response.text.strip()
        if clean_text.startswith('```json'):
            clean_text = clean_text[7:-3]
        elif clean_text.startswith('```'):
            clean_text = clean_text[3:-3]

        logger.debug(f"Resposta limpa do Gemini: {clean_text}")

        resultado = json.loads(clean_text)
        
        valid_intentions = ["withdraw_request", "withdraw_confirm", "doubt", "stock_query", "not_understood", "normal"]
        valid_analyses = ["normal", "low_stock_alert", "critical_stock_alert", "outlier_withdraw_request", "ambiguous", "not_understood", "greeting", "farewell"]
        
        if resultado.get("intention") not in valid_intentions:
            logger.warning(f"IA retornou intention inv√°lida: {resultado.get('intention')}")
            resultado["intention"] = "not_understood"
            
        if resultado.get("stella_analysis") not in valid_analyses:
            logger.warning(f"IA retornou stella_analysis inv√°lida: {resultado.get('stella_analysis')}")
            resultado["stella_analysis"] = "not_understood"
            
        # Pr√©-valida√ß√£o para pedidos de retirada: informa disponibilidade antes da confirma√ß√£o
        if resultado.get("intention") == "withdraw_request":
            pedidos = resultado.get("items", [])
            if pedidos:
                def _norm(s: str) -> str:
                    s = (s or "").strip().lower()
                    for ch in [" ", ",", ".", ";", ":", "/", "\\"]:
                        s = s.replace(ch, "_")
                    while "__" in s:
                        s = s.replace("__", "_")
                    return s.strip("_")

                faltantes = []
                insuficientes = []
                disponiveis = []
                for it in pedidos:
                    name = it.get("productName") or ""
                    qty = int(it.get("quantity", 0))
                    norm = _norm(name)
                    stock_item = external_stock.get(norm)
                    if not stock_item:
                        faltantes.append(name)
                        continue
                    available = int(stock_item.get("quantity", 0))
                    if qty > available:
                        insuficientes.append({
                            "name": name,
                            "requested": qty,
                            "available": available
                        })
                    else:
                        disponiveis.append({
                            "name": name,
                            "requested": qty,
                            "available": available
                        })

                info_msgs = []
                if disponiveis:
                    info_msgs.append(
                        "Dispon√≠vel: " + ", ".join([f"{d['name']} ({d['requested']}/{d['available']})" for d in disponiveis])
                    )
                if insuficientes:
                    info_msgs.append(
                        "Estoque insuficiente: " + ", ".join([f"{d['name']} (solicitado {d['requested']}, dispon√≠vel {d['available']})" for d in insuficientes])
                    )
                if faltantes:
                    info_msgs.append("N√£o encontrado(s): " + ", ".join(faltantes))

                if info_msgs:
                    complemento = " | ".join(info_msgs)
                    resultado["stella_analysis"] = resultado.get("stella_analysis", "normal")
                    resultado["response"] = f"{resultado.get('response', '')} (Checagem de estoque: {complemento})".strip()

        if resultado.get("intention") == "withdraw_confirm":
            items_confirmados = resultado.get("items", [])
            if items_confirmados:
                # valida itens contra estoque externo
                def _norm(s: str) -> str:
                    s = (s or "").strip().lower()
                    for ch in [" ", ",", ".", ";", ":", "/", "\\"]:
                        s = s.replace(ch, "_")
                    while "__" in s:
                        s = s.replace("__", "_")
                    return s.strip("_")

                faltantes = []
                insuficientes = []
                for it in items_confirmados:
                    name = it.get("productName") or ""
                    qty = int(it.get("quantity", 0))
                    norm = _norm(name)
                    stock_item = external_stock.get(norm)
                    if not stock_item:
                        faltantes.append(name)
                        continue
                    if qty > int(stock_item.get("quantity", 0)):
                        insuficientes.append({
                            "name": name,
                            "requested": qty,
                            "available": int(stock_item.get("quantity", 0))
                        })

                if faltantes or insuficientes:
                    # N√£o publica e orienta usu√°rio
                    msg_parts = []
                    if faltantes:
                        msg_parts.append(f"Itens n√£o encontrados: {', '.join(faltantes)}")
                    if insuficientes:
                        det = ", ".join([f"{d['name']} (solicitado {d['requested']}, dispon√≠vel {d['available']})" for d in insuficientes])
                        msg_parts.append(f"Itens com estoque insuficiente: {det}")
                    resultado["intention"] = "doubt"
                    resultado["stella_analysis"] = "ambiguous"
                    resultado["response"] = (
                        "N√£o consegui confirmar a retirada. " + "; ".join(msg_parts) + 
                        ". Deseja ajustar a quantidade ou escolher outro item?"
                    )
                else:
                    await publish_withdraw_confirm(session_id, items_confirmados)
                    logger.info(f"üì§ Publica√ß√£o de confirma√ß√£o iniciada | Sess√£o: {session_id}")
        
        logger.success(f"SessionID: {session_id} - Comando interpretado: {resultado.get('intention', 'N/A')}")
        return resultado
        
    except Exception as e:
        logger.error(f"Erro ao interpretar (sess√£o {session_id}): {e}")
        return {
            "intention": "not_understood",
            "items": [],
            "response": "Houve um erro no processamento. Pode repetir sua solicita√ß√£o?",
            "stella_analysis": "not_understood"
        }

